class Solution(object):
    def sampleStats(self, count):
        """
        :type count: List[int]
        :rtype: List[float]
        """
        n = 0
        min_ = None
        max_ = None
        sum_ = 0
        mode = [0, -1]
        cdf = []
        for i, cnt in enumerate(count):
            if cnt > 0:
                if min_ is None:
                    min_ = i
                max_ = i
                n += cnt
                sum_ += i * cnt
                if cnt > mode[0]:
                    mode = [cnt, i]
                cdf.append((i, n))
        mean = float(sum_) / n
        print(cdf)

        def find(k):
            for item in cdf:
                i, n = item
                if n > k:
                    return i

        if n % 2 == 1:
            median = find(n // 2)
        else:
            median = float(find(n // 2) + find(n // 2 - 1)) / 2
        return [float(min_), float(max_), mean, float(median), float(mode[1])]


